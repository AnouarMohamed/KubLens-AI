import fs from "node:fs";
import path from "node:path";
import yaml from "js-yaml";
import prettier from "prettier";

const root = process.cwd();
const args = new Set(process.argv.slice(2));
const checkMode = args.has("--check");

const specPath = path.join(root, "backend", "internal", "httpapi", "openapi.yaml");
const outPath = path.join(root, "src", "lib", "api", "generated", "openapi-contract.ts");

if (!fs.existsSync(specPath)) {
  console.error(`api-contract: missing OpenAPI spec at ${specPath}`);
  process.exit(1);
}

const parsed = yaml.load(fs.readFileSync(specPath, "utf8"));
if (!parsed || typeof parsed !== "object" || !("paths" in parsed)) {
  console.error("api-contract: OpenAPI spec is missing a top-level paths map");
  process.exit(1);
}

const paths = parsed.paths;
if (!paths || typeof paths !== "object") {
  console.error("api-contract: invalid paths map in OpenAPI spec");
  process.exit(1);
}

const pathTemplates = Object.keys(paths)
  .filter((value) => typeof value === "string" && value.trim() !== "")
  .sort((a, b) => a.localeCompare(b));

if (pathTemplates.length === 0) {
  console.error("api-contract: OpenAPI spec contains zero paths");
  process.exit(1);
}

const methodsByPath = {};
for (const template of pathTemplates) {
  const entry = paths[template];
  const methods =
    entry && typeof entry === "object"
      ? Object.keys(entry)
          .filter((method) => typeof method === "string")
          .map((method) => method.toUpperCase())
          .sort((a, b) => a.localeCompare(b))
      : [];
  methodsByPath[template] = methods;
}

const rendered = `${[
  "/* AUTO-GENERATED FILE. DO NOT EDIT. */",
  "/* Generated by scripts/generate-api-contract.mjs from backend/internal/httpapi/openapi.yaml. */",
  "",
  "export const OPENAPI_PATH_TEMPLATES = [",
  ...pathTemplates.map((template) => `  ${JSON.stringify(template)},`),
  "] as const;",
  "",
  "export type OpenAPIPathTemplate = (typeof OPENAPI_PATH_TEMPLATES)[number];",
  "",
  "export const OPENAPI_METHODS_BY_PATH = {",
  ...pathTemplates.map((template) => {
    const methods = methodsByPath[template];
    return `  ${JSON.stringify(template)}: [${methods.map((method) => JSON.stringify(method)).join(", ")}],`;
  }),
  "} as const;",
  "",
  "export type OpenAPIMethodForPath<TPath extends OpenAPIPathTemplate> =",
  "  (typeof OPENAPI_METHODS_BY_PATH)[TPath][number];",
  "",
].join("\n")}`;
const resolvedPrettierConfig = (await prettier.resolveConfig(outPath)) ?? {};
const formatted = await prettier.format(rendered, {
  ...resolvedPrettierConfig,
  endOfLine: "lf",
  filepath: outPath,
  parser: "typescript",
});

if (checkMode) {
  if (!fs.existsSync(outPath)) {
    console.error("api-contract: generated file is missing. Run `npm run generate:api-contract`.");
    process.exit(1);
  }

  const existing = fs.readFileSync(outPath, "utf8");
  if (existing !== formatted) {
    console.error("api-contract: generated contract is out of date. Run `npm run generate:api-contract`.");
    process.exit(1);
  }

  console.log(`api-contract-check: OK (${pathTemplates.length} paths)`);
  process.exit(0);
}

fs.mkdirSync(path.dirname(outPath), { recursive: true });
fs.writeFileSync(outPath, formatted, "utf8");
console.log(`api-contract-generate: OK (${pathTemplates.length} paths)`);
